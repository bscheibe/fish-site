                    <h4>HISTORY</h4>The area around Cabo has been inhabited for at least 10000 years.  The first Europeans found primitive people in the area living on a subistence diet from hunting and fishing, and only using stone tools.
                    <br><br>The modern history of Los Barriles is about fishing. From the 1950s fisherman came to the East Cape area in enough numbers that a couple of hotels were built in Los Barriles and Buena Vista.  With the completion of Mexico Highway 1 in 1978, more fishermn were able to drive down the peninsula and experience the great fishing of Los Barriles and the East Cape.  Windsurfers also came south and found excellent sailing conditions in the winter months.
                    <br><br>
                      Today Los Barriles attracts Fishermen, Windsurfers, Kite Boarders, and Divers from all over the world.  Fisherman come fro the marling, tuna, and dorado that team in nearby waters.  Windsurfers and Kite Boarders come for the famous North Wind which blows from November thru March.  And Divers cherish the teeming waters of Cabo Pulmo Marine Park.
                    <br>
                    <br>
                    <h4>PEOPLE</h4>The population of Los Barriles is now a mix of Mexican and North Americans who live full or part time in the town. Many of the Mexicans in Los Barriles have lived their for generations.  It has a small town feel with everyone knowing each other and many people making a living from tourism, fishing, and the sea.
                    <br><br>
                    <h4>TOURISM</h4>The major attractions in Los Barriles revolve around the beautiful Sea of Cortez.  From April thru October, <a href="http://www.verduogsbeachresort.com/fishing_packaages.html">Los Barriles is a place for fishing</a>. Large pelagic fish can be found very close to shore. The water gets very deep quickly, and the area is blessed with an undersea canyon that gets very close to the beach. Tuna, Marlin, Dorado (Mahi-Mahi), Wahoo, Sailfish, and Roosterfish can all be found in good numbers not far from shore. Anglers from around the world come to enjoy this great fishing.
                    <br><br>
                    From November thru March, Los Barriles is a windsurfer and kite boarder paradise. The "North Wind" blows steadyily at times during these months and can literally blow for days. While it's bad for fishing, it's ideal for windsurfing.
                    <br><br>Throughout the year Los Barriles is a great place for snorkeling and diving. Snorkelers can have fun snorkeling right in front of Martin Verdug's Hotel. They can also book a trip to snorkel and dive the world famous Cabo Pulmo reef.  Follow this link for more information on <a href="http://www.verdugosbeachresort.com/activities.php">Los Barriles activites</a>.
                    
                    
