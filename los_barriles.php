                    <h4>LOCATION</h4>Los Barriles is located on the east side of the southern tip of Baja California Sur, on the Sea of Cortez.  It is about one hour north San Jose del Cabo, and an hour south of La Paz.  The area around Los Barriles is generally known as the East Cape. The closets aiports are Los Cabos, and La Paz, with Los Cabos having more frequent flights.  Click for <a href="http://www.verdugosbeachresort.com/maps.php">Transportation and Maps to Los Barriles</a>
